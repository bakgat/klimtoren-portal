<p><strong>Exentra</strong> is hét expertisecentrum rond begaafdheidsonderzoek. Onze school wordt gecoacht
    om nog sterker te kunnen differentiëren voor élk kind.</p>

<p><strong>KiVa</strong> is het enige wetenschappelijk onderbouwd anti-pestprogramma. Het bewees al zijn
    effect in Finland en Nederland. Kinderen leren dat groepsdynamiek bepalend is voor de fijne sfeer op
    school.</p>

<p><strong>CLB</strong> Brugge(n) is het Centrum voor LeerlingBegeleiding dat onze school bijstaat. Het CLB
    is aanwezig bij zorgoverleggen, gaat gesprekken aan met ouders, leerlingen en leerkrachten. Ze staan ook
    in voor het gratis medisch onderzoek.</p>